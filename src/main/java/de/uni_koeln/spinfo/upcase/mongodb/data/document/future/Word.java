package de.uni_koeln.spinfo.upcase.mongodb.data.document.future;

import java.util.Date;
import java.util.Set;
import java.util.TreeSet;

import org.springframework.data.annotation.Id;
import org.springframework.data.annotation.Transient;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "ref_words")
public class Word {
	
	@Transient
	public static final String COLLECTION = "ref_words";
	
	@Id private String id;
	
	private String token;
	private Date lastModified;
	
	// ANNOTATION IDS
	private Set<Annotation> annotations;
	private Box box;
	
	public Word() {
		super();
	}
	
	public Word(String token, Box box) {
		super();
		this.token = token;
		this.lastModified = new Date();
		this.annotations = new TreeSet<>();
		this.box = box;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public Date getLastModified() {
		return lastModified;
	}

	public void setLastModified(Date lastModified) {
		this.lastModified = lastModified;
	}

	public Set<Annotation> getAnnotations() {
		return annotations;
	}
	
	public void setAnnotation(Annotation annotation) {
		this.annotations.add(annotation);
	}

	public void setAnnotations(Set<Annotation> annotations) {
		this.annotations = annotations;
	}

	public Box getBox() {
		return box;
	}

	public void setBox(Box box) {
		this.box = box;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((lastModified == null) ? 0 : lastModified.hashCode());
		result = prime * result + ((token == null) ? 0 : token.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Word other = (Word) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (lastModified == null) {
			if (other.lastModified != null)
				return false;
		} else if (!lastModified.equals(other.lastModified))
			return false;
		if (token == null) {
			if (other.token != null)
				return false;
		} else if (!token.equals(other.token))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Word [id=" + id + ", token=" + token + ", annotations=" + annotations + ", box=" + box + "]";
	}
	
	
}
